                <p style="font-size: 17px;">L'Antarctique, situé à l'extrême sud de notre planète, est un continent recouvert de glace, entouré par l'océan Austral. C'est la région la plus froide, la plus sèche et la plus élevée du monde. Avec une superficie de 14 millions de kilomètres carrés, l'Antarctique est presque entièrement couvert de glace, ce qui en fait le lieu le plus froid sur Terre. Sa température naturelle la plus basse, -93,2 °C, a été enregistrée sur ce continent. Il est entouré des mers de Ross et de Weddell, et selon les classifications, des océans Atlantique, Indien et Pacifique ou du seul océan Austral. L'Antarctique est le continent dont le climat est le plus froid, le plus sec et le plus venteux, et il constitue le cœur de la région antarctique, qui inclut également les parties émergées du plateau des Kerguelen ainsi que d'autres territoires insulaires de la plaque antarctique plus ou moins proches. Cette région est dédiée à la paix et à la science depuis la signature du Traité sur l'Antarctique en 1959. L'Antarctique n'a jamais été peuplé par l'homme en raison de son climat extrême, mais des scientifiques y habitent par roulement pour y mener des études et des expériences.</p>
